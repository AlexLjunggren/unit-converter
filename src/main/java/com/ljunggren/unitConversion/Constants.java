package com.ljunggren.unitConversion;

public class Constants {

    public static final double METER_TO_FOOT = 3.28084;
    public static final double FOOT_TO_METER = 1 / METER_TO_FOOT;
    
    public static final double LITER_TO_OUNCE = 33.8140226;
    public static final double OUNCE_TO_LITER = 1 / LITER_TO_OUNCE;
    
    public static final double POUND_TO_NEWTON = 4.4482216152605;
    public static final double NEWTON_TO_POUND = 1 / POUND_TO_NEWTON;
    
    public static final double KILOGRAM_TO_POUND = 2.20462262185;
    public static final double POUND_TO_KILOGRAM = 1 / KILOGRAM_TO_POUND;
    
}
